package application;

/**
 * The class represents an individual part of the snake.
 */
public class Corner {

	int x;
	int y;

	/**
	 * Creates a new corner that represents the part of the snake with the x and y
	 * coordinates
	 * 
	 * @param x coordinate.
	 * @param y coordinate.
	 */
	public Corner(int x, int y) {
		this.x = x;
		this.y = y;
	}
}
